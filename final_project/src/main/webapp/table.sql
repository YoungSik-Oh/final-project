CREATE TABLE PUSERS (
 ID VARCHAR2(50) PRIMARY KEY,
 PID VARCHAR2(50),
 PWD VARCHAR2(100) NOT NULL,
 NAME VARCHAR2(20) NOT NULL,
 PHONE VARCHAR2(30) NOT NULL,
 ADDR VARCHAR2(100) NOT NULL,
 FAVORITE VARCHAR2(300),
 EMAIL VARCHAR2(100),
 NAVEREMAIL VARCHAR2(100),
 UNIQID VARCHAR2(30),
 ISADMIN VARCHAR2(4),
 REGDATE DATE
 );